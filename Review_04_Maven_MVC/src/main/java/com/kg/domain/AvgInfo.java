package com.kg.domain;

public class AvgInfo {

	private String name;
	private double avg;
	
	public AvgInfo() {}
	
	

	public AvgInfo(String name, double avg) {
		super();
		this.name = name;
		this.avg = avg;
	}



	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getAvg() {
		return avg;
	}

	public void setAvg(double avg) {
		this.avg = avg;
	}

	
	
	
	
	
	
	
	
}
